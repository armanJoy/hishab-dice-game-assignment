package com.hishab.dice.model.view;

public class PlayerDef {

    private String name;

    private Integer age;

    public PlayerDef() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

}
